class Tree
{
    static int max_lev=0;
    void logic(Node root,int lev)
    {
        if(root==null)
        return;
        
        if(max_lev<lev)
        {
            System.out.print(root.data+" ");
            max_lev=lev;
        }
        
        logic(root.left,lev+1);
        logic(root.right,lev+1);
    }
    void leftView(Node root)
    {
        max_lev=0;
      logic(root,1);
    }
}